# Define a function named calculate that takes three arguments: number1, number2, number3
def calculate(number1, number2, number3):
    # Calculate the sum of number1 and number2, and then multiply the result by number3
    result = (number1 + number2) * number3
    # Return the calculated result
    return result

# Call the calculate function with arguments 5, 4, and 5 and print the result
print(calculate(5, 4, 5))
# Call the calculate function with arguments 8, 11, and 7 and print the result
print(calculate(8, 11, 7))
# Call the calculate function with arguments 15, 99, and 10 and print the result
print(calculate(15, 99, 10))