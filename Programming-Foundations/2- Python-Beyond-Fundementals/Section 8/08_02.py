# Define a list of colors
colors = [
    "red",      # The color red
    "green",    # The color green
    "blue",     # The color blue
    "white",    # The color white
    "black",    # The color black
    "red",      # The color red (duplicated)
]

# Print a copy of the colors list
print(colors.copy())

# Print the length of the colors list
print(len(colors))

# Print the number of times "red" appears in the colors list
print(colors.count("red"))

# Add "offwhite" to the end of the colors list
colors.append("offwhite")

# Print the updated colors list
print(colors)